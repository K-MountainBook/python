# 二つの整数値を読み込んで加減乗除（その２：読込みと変換を単一の文で行う）

a = int(input('整数a：'))
b = int(input('整数b：'))

print('a + b は',  a + b,  'です。')
print('a - b は',  a - b,  'です。')
print('a * b は',  a * b,  'です。')
print('a / b は',  a / b,  'です。')
print('a // b は', a // b, 'です。')
print('a % b は',  a % b,  'です。')
print('a ** b は', a ** b, 'です。')
