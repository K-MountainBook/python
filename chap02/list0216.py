# 二つの整数値を読み込んで加減乗除（その４：formatを利用して詰めて表示）

a = int(input('整数a：'))
b = int(input('整数b：'))

print('a + b は{}です。'.format(a + b))
print('a - b は{}です。'.format(a - b))
print('a * b は{}です。'.format(a * b))
print('a / b は{}です。'.format(a / b))
print('a // b は{}です。'.format(a // b))
print('a % b は{}です。'.format(a % b))
print('a ** b は{}です。'.format(a ** b))
