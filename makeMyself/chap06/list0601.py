# 文字列内の全文字を順に表示（その１）

s = 'ABCDEFG'

print('正の数のインデックス')
print('s[0] = ', s[0])
print('s[1] = ', s[1])
print('s[2] = ', s[2])
print('s[3] = ', s[3])
print('s[4] = ', s[4])
print('s[5] = ', s[5])
print('s[5] = ', s[6])

print('負の数のインデックス')
print('s[0] = ', s[-7])
print('s[1] = ', s[-6])
print('s[2] = ', s[-5])
print('s[3] = ', s[-4])
print('s[4] = ', s[-3])
print('s[5] = ', s[-2])
print('s[5] = ', s[-1])
